/**
 * Agents CLI command — auto-generate platform-specific discovery files.
 *
 * Command:
 *   unicli agents generate  — Generate discovery files from adapter registry
 *
 * Options:
 *   --output <path>  — Output file path (default: stdout)
 *   --for <platform> — Target platform format
 *
 * Supported platforms:
 *   generic    — Standard AGENTS.md (default)
 *   claude     — CLAUDE.md snippet with MCP config JSON
 *   codex      — AGENTS.md + .codex/config.toml snippet
 *   openclaw   — ClawHub-compatible JSON metadata
 *   hermes     — Context file + config.yaml snippet
 *   opencode   — opencode.jsonc snippet
 *   cursor     — .cursorrules format
 *   goose      — Goose recipe YAML
 */

import { Command } from "commander";
import chalk from "chalk";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { getAllAdapters, listCommands } from "../registry.js";
import { VERSION } from "../constants.js";
import type { AdapterManifest } from "../types.js";

// ── Template context shared by all formatters ─────────────────────────────

interface TemplateContext {
  sites: number;
  commands: number;
  version: string;
  date: string;
  adapters: AdapterManifest[];
  commandList: ReturnType<typeof listCommands>;
}

// ── MCP config snippet (reused by multiple platforms) ─────────────────────

function mcpConfigObject(): Record<string, unknown> {
  return {
    command: "npx",
    args: ["-y", "@zenalexa/unicli", "mcp", "serve"],
    env: {},
  };
}

// ── Platform format generators ────────────────────────────────────────────

function formatGeneric(ctx: TemplateContext): string {
  const lines: string[] = [];

  lines.push(
    `<!-- unicli v${ctx.version} | ${ctx.sites} sites | ${ctx.commands} commands -->`,
  );
  lines.push("# AGENTS.md — Uni-CLI Capability Discovery");
  lines.push("");
  lines.push("> Auto-generated. Do not edit manually.");
  lines.push("> Regenerate with: `unicli agents generate`");
  lines.push("");

  // Installation
  lines.push("## Installation");
  lines.push("");
  lines.push("```bash");
  lines.push("npm i -g @zenalexa/unicli");
  lines.push("```");
  lines.push("");

  // Sites by Category
  lines.push("## Sites by Category");
  lines.push("");

  const categories = new Map<string, AdapterManifest[]>();
  for (const adapter of ctx.adapters) {
    const cat = adapter.category ?? "Uncategorized";
    const group = categories.get(cat);
    if (group) {
      group.push(adapter);
    } else {
      categories.set(cat, [adapter]);
    }
  }

  const sortedCategories = Array.from(categories.entries()).sort(([a], [b]) =>
    a.localeCompare(b),
  );

  for (const [category, adaptersInCat] of sortedCategories) {
    lines.push(`### ${category}`);
    lines.push("");
    lines.push("| Site | Commands | Auth | Description |");
    lines.push("|------|----------|------|-------------|");

    const sorted = adaptersInCat.sort((a, b) => a.name.localeCompare(b.name));

    for (const adapter of sorted) {
      const cmdNames = Object.keys(adapter.commands).sort().join(", ");
      const auth = adapter.strategy ?? "public";
      const desc = adapter.description ?? "";
      lines.push(`| ${adapter.name} | ${cmdNames} | ${auth} | ${desc} |`);
    }

    lines.push("");
  }

  // All Commands
  lines.push("## All Commands");
  lines.push("");
  lines.push("| Site | Command | Description | Auth |");
  lines.push("|------|---------|-------------|------|");

  for (const cmd of ctx.commandList) {
    const auth = cmd.auth ? "yes" : "no";
    lines.push(
      `| ${cmd.site} | ${cmd.command} | ${cmd.description} | ${auth} |`,
    );
  }

  lines.push("");

  // Self-repair
  lines.push("## Self-Repair");
  lines.push("");
  lines.push(
    "When a command fails, unicli outputs structured JSON to stderr with `adapter_path` and `suggestion` fields.",
  );
  lines.push(
    "Read the YAML adapter at that path to understand and fix the issue.",
  );
  lines.push("Run `unicli repair <site> <command>` for automated repair.");
  lines.push("");

  lines.push("---");
  lines.push(`<!-- Generated by Uni-CLI v${ctx.version} on ${ctx.date} -->`);
  lines.push("");

  return lines.join("\n");
}

