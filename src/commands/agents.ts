// ── Platform registry ─────────────────────────────────────────────────────

const PLATFORMS = [
  "generic",
  "claude",
  "codex",
  "openclaw",
  "hermes",
  "opencode",
  "cursor",
  "goose",
] as const;

type Platform = (typeof PLATFORMS)[number];

const formatters: Record<Platform, (ctx: TemplateContext) => string> = {
  generic: formatGeneric,
  claude: formatClaude,
  codex: formatCodex,
  openclaw: formatOpenclaw,
  hermes: formatHermes,
  opencode: formatOpencode,
  cursor: formatCursor,
  goose: formatGoose,
};

// ── Command registration ──────────────────────────────────────────────────

export function registerAgentsCommand(program: Command): void {
  const agents = program
    .command("agents")
    .description("AGENTS.md generation and management");

  agents
    .command("generate")
    .description("Auto-generate discovery files from loaded adapters")
    .option("--output <path>", "write to file instead of stdout")
    .option(
      "--for <platform>",
      `target platform (${PLATFORMS.join("|")})`,
      "generic",
    )
    .action((opts: { output?: string; for: string }) => {
      const platform = opts.for as Platform;
      if (!PLATFORMS.includes(platform)) {
        console.error(
          chalk.red(
            `Unknown platform: ${platform}. Available: ${PLATFORMS.join(", ")}`,
          ),
        );
        process.exitCode = 1;
        return;
      }

      const adapters = getAllAdapters();
      const commandList = listCommands();

      if (adapters.length === 0) {
        console.error(chalk.red("No adapters loaded"));
        process.exit(1);
      }

      const ctx: TemplateContext = {
        sites: adapters.length,
        commands: commandList.length,
        version: VERSION,
        date: new Date().toISOString().split("T")[0],
        adapters,
        commandList,
      };

      const content = formatters[platform](ctx);

      if (opts.output) {
        const outPath = resolve(opts.output);
        writeFileSync(outPath, content, "utf-8");
        console.error(
          chalk.green(
            `Generated ${outPath} (${ctx.sites} sites, ${ctx.commands} commands, format: ${platform})`,
          ),
        );
      } else {
        // Print to stdout so agents can redirect
        process.stdout.write(content);
      }
    });
}
